    static let gold = Color(red: 0.85, green: 0.70, blue: 0.28)
    static let cut = Color(red: 0.80, green: 0.18, blue: 0.18)
    static let ink = Color(red: 0.06, green: 0.06, blue: 0.07)
    static let parchment = Color(red: 0.93, green: 0.91, blue: 0.86)
}
